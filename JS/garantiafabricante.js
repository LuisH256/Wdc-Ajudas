function pesquisarGarantia() {
    const fabricante = document.getElementById('fabricante').value;
    const tempoGarantia = document.getElementById('tempo-garantia');
    const rmaFabricante = document.getElementById('rma-fabricante');
    const mensagemErro = document.getElementById('mensagem-erro');
    const mensagemAdicional = document.getElementById('mensagem-adicional');
    const copiarInformacoes = document.getElementById('copiar-informacoes');

    if (garantias[fabricante]) {
        tempoGarantia.innerHTML = `<strong>Tempo de Garantia:</strong> ${garantias[fabricante].tempo}`;
        rmaFabricante.innerHTML = `<strong>RMA:</strong> ${garantias[fabricante].rma}`;
        mensagemErro.style.display = 'none';
        mensagemAdicional.textContent = garantias[fabricante].mensagem.trim() || '';
        copiarInformacoes.style.display = 'block';
    } else {
        tempoGarantia.textContent = '';
        rmaFabricante.textContent = '';
        mensagemErro.style.display = 'block';
        mensagemAdicional.innerHTML = '';
        copiarInformacoes.style.display = 'none';
    }
}

function copiarInformacoes() {
    const mensagemAdicional = document.getElementById('mensagem-adicional').textContent.trim();
    const textarea = document.createElement('textarea');
    textarea.value = mensagemAdicional;
    document.body.appendChild(textarea);
    textarea.select();
    document.execCommand('copy');
    document.body.removeChild(textarea);
    alert('Informações copiadas para a área de transferência.');
}
